function categorizeIngredient(name) {
  const lower = name.toLowerCase();
  for (const [cat, keywords] of Object.entries(ingredientCategories)) {
    if (keywords.some(kw => lower.includes(kw))) return cat;
  }
  return 'other';
}

export function generateWeeklyShoppingList(weeklyMealPlan, excludedFoods = []) {
  if (!weeklyMealPlan) return {};

  const items = {};
  const days = Array.isArray(weeklyMealPlan) ? weeklyMealPlan : [];

  if (days.length === 0) return {};

  for (const dayPlan of days) {
    if (!dayPlan) continue;
    const meals = [dayPlan.breakfast, dayPlan.lunch, dayPlan.dinner, dayPlan.snack];
    for (const meal of meals) {
      if (!meal) continue;
      for (const ingStr of meal.ingredients) {
        const { name, quantity } = parseIngredient(ingStr);
        const { amount, unit } = parseQuantity(quantity);
        const key = name.toLowerCase();

        if (items[key]) {
          items[key].weeklyAmount += amount;
        } else {
          items[key] = {
            name,
            dailyQuantity: quantity,
            weeklyAmount: amount,
            unit,
            category: categorizeIngredient(name),
            isExcluded: excludedFoods.some(ex => key.includes(ex.toLowerCase())),
          };
        }
      }
    }
  }

  const grouped = {};
  for (const item of Object.values(items)) {
    const cat = item.category;
    if (!grouped[cat]) grouped[cat] = [];
    grouped[cat].push(item);
  }

  return grouped;
}

export function getReplacementsFor(ingredientName) {
  const lower = ingredientName.toLowerCase();
  for (const [key, val] of Object.entries(foodReplacements)) {
    if (lower.includes(key)) return val.alternatives;
  }
  return null;
}

export function findMealsWithout(excludedFoods, mealType) {
  const meals = mealDatabase[mealType];
  return meals.filter(meal =>
    !meal.ingredients.some(ing =>
      excludedFoods.some(ex => ing.toLowerCase().includes(ex.toLowerCase()))
    )
  );
}

function filterExcluded(meals, excludedFoods) {
  if (!excludedFoods.length) return meals;
  const filtered = meals.filter(meal =>
    !meal.ingredients.some(ing =>
      excludedFoods.some(ex => ing.toLowerCase().includes(ex.toLowerCase()))
    )
  );
  return filtered.length > 0 ? filtered : meals;
}

function pickRandom(meals, target) {
  const sorted = [...meals].sort((a, b) => Math.abs(a.calories - target) - Math.abs(b.calories - target));
  const top = sorted.slice(0, Math.min(3, sorted.length));
  return top[Math.floor(Math.random() * top.length)];
}

function pickRandomExcluding(meals, target, usedNames) {
  const available = meals.filter(m => !usedNames.has(m.name));
  if (available.length === 0) return pickRandom(meals, target);
  return pickRandom(available, target);
}

export function generateMealPlan(targetCalories, goal, excludedFoods = []) {
  const cals = targetCalories;
  const breakfastTarget = cals * 0.25;
  const lunchTarget = cals * 0.35;
  const dinnerTarget = cals * 0.30;
  const snackTarget = cals * 0.10;

  const plan = {
    breakfast: pickRandom(filterExcluded(mealDatabase.breakfast, excludedFoods), breakfastTarget),
    lunch: pickRandom(filterExcluded(mealDatabase.lunch, excludedFoods), lunchTarget),
    dinner: pickRandom(filterExcluded(mealDatabase.dinner, excludedFoods), dinnerTarget),
    snack: pickRandom(filterExcluded(mealDatabase.snack, excludedFoods), snackTarget),
  };

  plan.totalCalories = plan.breakfast.calories + plan.lunch.calories + plan.dinner.calories + plan.snack.calories;
  plan.totalProtein = plan.breakfast.protein + plan.lunch.protein + plan.dinner.protein + plan.snack.protein;
  plan.totalCarbs = plan.breakfast.carbs + plan.lunch.carbs + plan.dinner.carbs + plan.snack.carbs;
  plan.totalFat = plan.breakfast.fat + plan.lunch.fat + plan.dinner.fat + plan.snack.fat;

  return plan;
}

export function generateWeeklyMealPlan(targetCalories, goal, excludedFoods = []) {
  const cals = targetCalories;
  const breakfastTarget = cals * 0.25;
  const lunchTarget = cals * 0.35;
  const dinnerTarget = cals * 0.30;
  const snackTarget = cals * 0.10;

  const bkAvail = filterExcluded(mealDatabase.breakfast, excludedFoods);
  const luAvail = filterExcluded(mealDatabase.lunch, excludedFoods);
  const diAvail = filterExcluded(mealDatabase.dinner, excludedFoods);
  const snAvail = filterExcluded(mealDatabase.snack, excludedFoods);

  const dayNames = ['Sunday', 'Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday'];
  const usedBk = new Set();
  const usedLu = new Set();
  const usedDi = new Set();
  const usedSn = new Set();

  const week = dayNames.map(dayName => {
    const breakfast = pickRandomExcluding(bkAvail, breakfastTarget, usedBk);
    const lunch = pickRandomExcluding(luAvail, lunchTarget, usedLu);
    const dinner = pickRandomExcluding(diAvail, dinnerTarget, usedDi);
    const snack = pickRandomExcluding(snAvail, snackTarget, usedSn);
    usedBk.add(breakfast.name);
    usedLu.add(lunch.name);
    usedDi.add(dinner.name);
    usedSn.add(snack.name);

    const totalCalories = breakfast.calories + lunch.calories + dinner.calories + snack.calories;
    const totalProtein = breakfast.protein + lunch.protein + dinner.protein + snack.protein;
    const totalCarbs = breakfast.carbs + lunch.carbs + dinner.carbs + snack.carbs;
    const totalFat = breakfast.fat + lunch.fat + dinner.fat + snack.fat;

    return { dayName, breakfast, lunch, dinner, snack, totalCalories, totalProtein, totalCarbs, totalFat };
  });

  return week;
}

export function getRandomMeals(mealType, count = 3) {
  const meals = mealDatabase[mealType];
  const shuffled = [...meals].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
