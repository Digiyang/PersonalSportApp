function parseIngredient(ingredientStr) {
  const match = ingredientStr.match(/^(.+?)\s*\(([^)]+)\)\s*$/);
  if (match) {
    return { name: match[1].trim(), quantity: match[2].trim() };
  }
  return { name: ingredientStr.trim(), quantity: '' };
}

function parseQuantity(qtyStr) {
  if (!qtyStr) return { amount: 1, unit: '' };
  const numMatch = qtyStr.match(/^([\d.½¼¾⅓⅔]+)\s*(.*)$/);
  if (numMatch) {
    let amount = numMatch[1];
    if (amount === '½') amount = 0.5;
    else if (amount === '¼') amount = 0.25;
    else if (amount === '¾') amount = 0.75;
    else amount = parseFloat(amount);
    return { amount: amount || 1, unit: numMatch[2].trim() };
  }
  return { amount: 1, unit: qtyStr };
}

